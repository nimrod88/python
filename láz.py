nevek=[]
lázak=[]
eredmeny=[]
for _ in range(3):
    név=input("Adja meg a nevét! ")
    láz=float(input("Hány fokos láza van? "))
    nevek.append(név)
    lázak.append(láz)
    print(nevek)
    print(lázak)
    if láz <= 37:
        print("Nem vagy lázas.")
        eredmeny.append[eredmeny]
    elif láz > 37 and láz <= 38:
        print("Átlagos.")
        eredmeny.append[eredmeny]
    else:
        print("Lázas vagy!")
        eredmeny.append[eredmeny]
print(eredmeny)
print(lázak.sort)